package cc.wulian.smarthomev5.entity;

public class CombindDeviceEntity {

	private String gwID;
	private String bindID;
	private String name;
	private String roomID;
	private String leftDevID;
	private String rightDevID;
	public String getGwID() {
		return gwID;
	}
	public void setGwID(String gwID) {
		this.gwID = gwID;
	}
	public String getBindID() {
		return bindID;
	}
	public void setBindID(String bindID) {
		this.bindID = bindID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRoomID() {
		return roomID;
	}
	public void setRoomID(String roomID) {
		this.roomID = roomID;
	}
	public String getLeftDevID() {
		return leftDevID;
	}
	public void setLeftDevID(String leftDevID) {
		this.leftDevID = leftDevID;
	}
	public String getRightDevID() {
		return rightDevID;
	}
	public void setRightDevID(String rightDevID) {
		this.rightDevID = rightDevID;
	}

}
